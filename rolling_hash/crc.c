static inline uint ullbyteWidth(register const ullong value) {
    if (value == 0)
        return 1;

    return (uint) (ULLONG_WIDTH - __builtin_clzll(value) + CHAR_BIT - 1) / CHAR_BIT;
}

static inline void *routine(void * const arg) {
    register const Arguments arguments = *(Arguments *) arg;
    for (ullong i = arguments.rank; i < arguments.limit; i += arguments.size) {
        register const uint length = ullbyteWidth(i),
            result = arguments.checksum((const uchar *) &i, length);
        atomic_fetch_add(arguments.result + result, 1);
    }
    return NULL;
}

int main(const int argc, char ** const argv) {
    EXIT_IF(argc != 2, "argc != 2");

    ullong limit;
    EXIT_IF(sscanf(argv[1], "%llu", &limit) != 1, "sscanf");

    const size_t resultSize = 1ULL << 32U;
    atomic_uint * const result =
        (atomic_uint *) malloc(resultSize * sizeof(atomic_int));
    EXIT_IF(result == NULL, "malloc");

    for (size_t i = 0; i < resultSize; ++i)
        atomic_init(result + i, 0);

    pthread_t threads[THREAD_NUMBER];
    Arguments arguments[THREAD_NUMBER];

    for (size_t i = 0; i < THREAD_NUMBER; ++i) {
        arguments[i] = (Arguments) {crc32, result, limit, (uint) i, THREAD_NUMBER};
        EXIT_IF(pthread_create(threads + i, NULL, routine, arguments + i) != 0, "pthread_create");
    }

    for (size_t i = 0; i < THREAD_NUMBER; ++i)
        EXIT_IF(pthread_join(threads[i], NULL) != 0, "pthread_join");

    uint minValue = UINT_MAX, maxValue = 0;
    for (size_t i = 0; i < resultSize; ++i) {
        const uint value = atomic_load(result + i);
        minValue = umin(minValue, value);
        maxValue = umax(maxValue, value);
    }

    const size_t countSize = maxValue - minValue + 1;
    uint * const restrict count = (uint *) calloc(countSize, sizeof(uint));
    EXIT_IF(count == NULL, "calloc");
    for (size_t i = 0; i < resultSize; ++i)
        ++count[atomic_load(result + i) - minValue];

    free(result);

    printf("min: %u, max: %u\n", minValue, maxValue);
    printf("[");
    for (size_t i = 0; i < countSize; ++i)
        if (i != 0)
            printf(", %u", count[i]);
        else
            printf("%u", count[i]);
    printf("]\n");

    free(count);

    return 0;
}
